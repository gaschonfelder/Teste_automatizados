/**
 * A classe Somar fornece um método para realizar a operação de adição entre dois números inteiros.
 */
public class Somar {
    
    /**
     * Realiza a operação de adição entre dois números inteiros.
     *
     * @param a O primeiro número a ser somado.
     * @param b O segundo número a ser somado.
     * @return O resultado da adição de 'a' e 'b'.
     */
    public int somar(int a, int b) {
        return a + b;
    }
}