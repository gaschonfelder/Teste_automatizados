/**
 * A classe Subtração fornece um método para realizar a operação de subtração entre dois números inteiros.
 */
public class Subtração {
    
    /**
     * Realiza a operação de subtração entre dois números inteiros.
     *
     * @param a O número do qual o segundo número será subtraído.
     * @param b O número que será subtraído do primeiro número.
     * @return O resultado da subtração de 'b' de 'a'.
     */
    public int sub(int a, int b) {
        return a - b;
    }
}